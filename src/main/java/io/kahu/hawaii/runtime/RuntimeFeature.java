package io.kahu.hawaii.runtime;

import java.io.Serializable;

public class RuntimeFeature implements Serializable {
    private static final long serialVersionUID = 1L;

    private final String name;
    private String value;

    public RuntimeFeature(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }

    void setValue(String value) {
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        RuntimeFeature that = (RuntimeFeature) o;

        if (name != null ? !name.equals(that.name) : that.name != null) {
            return false;
        }
        if (value != null ? !value.equals(that.value) : that.value != null) {
            return false;
        }

        return true;
    }

    @Override
    public int hashCode() {
        int result = name != null ? name.hashCode() : 0;
        result = 31 * result + (value != null ? value.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "RuntimeFeature{" + "name='" + name + '\'' + ", value='" + value + '\'' + '}';
    }

}
